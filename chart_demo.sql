/*!50003 CREATE DEFINER=`root`@`%` PROCEDURE `ist_calendar`()
BEGIN
  DECLARE i INT;
  SET i = 0;
  WHILE
    i < 365 DO
    INSERT INTO t_calendar (DATE,count)
    VALUES
      (DATE_SUB(STR_TO_DATE('2018-01-01','%Y-%m-%d'), INTERVAL - i DAY),FLOOR( 500 + RAND() * 20000));
    SET i = i + 1;
  END WHILE;
END */$$
